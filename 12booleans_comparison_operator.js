// booleans and comparison operator

//booleans
//true,false

let num1=8;
let num2=7;
console.log(num1>=num2);
console.log(num1==num2);

// == -> checks the value only and === checks the data type
num1="7";
num2=7;
console.log(num1==num2);
console.log(num1===num2);

// != -> value check vs !== ->data type check
console.log(num1!==num2);
